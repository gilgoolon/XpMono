#pragma once
#include <cstdint>
#include <sstream>

enum class ErrorCode : uint8_t
{
	UNKNOWN_ERROR = 0,

	// Utilities
	FAILED_TIME_FORMAT_CONVERSION,
	FAILED_GET_COMMAND_LINE,
	FAILED_STRING_CONVERSION,

	// Generic
	UNCOVERED_ENUM_VALUE,
	OUT_OF_BOUNDS,
	INVALID_ARGUMENT,

	// Files
	FAILED_FILE_CREATE,
	FAILED_FILE_WRITE,
	FAILED_FILE_SEEK,
	FAILED_FILE_READ,

	// Process
	FAILED_PROCESS_OPEN,
	FAILED_PROCESS_QUERY_INFORMATION,
	FAILED_PROCESS_READ_MEMORY,
	FAILED_PROCESS_GET_PID,
	FAILED_PROCESS_GET_PATH,
	FAILED_PROCESS_GET_TIMES,
	FAILED_PROCESS_CREATE,
	FAILED_PROCESS_TERMINATE,
	FAILED_PROCESS_GET_MODULES,
	FAILED_PROCESS_GET_MAIN_MODULE,
	FAILED_PROCESS_GET_MODULE_FILENAME,

	// Thread
	FAILED_THREAD_CREATE,

	// Remote Memory
	FAILED_REMOTE_MEMORY_ALLOCATE,
	FAILED_REMOTE_MEMORY_WRITE,

	// Library
	FAILED_LIBRARY_GET_PROC_ADDRESS,
	FAILED_LIBRARY_LOAD,

	// Events
	FAILED_EVENT_OPEN,
	FAILED_EVENT_CREATE,
	FAILED_WAIT,

	// Process Iterator
	FAILED_PROCESS_ITERATOR_CREATE,
	FAILED_PROCESS_ITERATOR_NEXT,

	// Apricot
	FAILED_APRICOT_LOAD,
	FAILED_APRICOT_GET,

	// Fig
	FAILED_FIG_INITIALIZE,
	FAILED_FIG_EXECUTE,
	FAILED_FIG_STATUS,
	FAILED_FIG_TAKE,
};

std::wostringstream& operator<<(std::wostringstream& output, ErrorCode code);
